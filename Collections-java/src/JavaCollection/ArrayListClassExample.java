package JavaCollection;

import java.util.ArrayList;
import java.util.List;

// this example demonstrate how to create an ArrayList using ArrayList() constructor
// and add new elements to an ArrayList using add() method.

public class ArrayListClassExample {
	public static void main(String[] args) {
		
		// create a ArrayList
		List <String> fruits=new ArrayList<>();
		fruits.add("Banana");
		fruits.add("Apple");
		fruits.add("grapes");
		fruits.add("Mango");
		System.out.println(fruits);
	}

}
